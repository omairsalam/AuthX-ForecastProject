    public static void getTagList(ArrayList<JFreeChart> chartList){
        for (JFreeChart s : chartList){
            tagArray.add(s.getTitle().getText());
        }
    } 
    
    public void populateComboBox(){
        for (String s : tagArray){
            rolePicker.addItem(s);
        }
    }
